/// Szybka Fucha spacing scale
/// Based on 4px base unit (matching szybkafucha.app)
abstract class AppSpacing {
  static const double space1 = 4;   // 0.25rem
  static const double space2 = 8;   // 0.5rem
  static const double space3 = 12;  // 0.75rem
  static const double space4 = 16;  // 1rem
  static const double space5 = 20;  // 1.25rem
  static const double space6 = 24;  // 1.5rem
  static const double space8 = 32;  // 2rem
  static const double space10 = 40; // 2.5rem
  static const double space12 = 48; // 3rem
  static const double space16 = 64; // 4rem
  static const double space20 = 80; // 5rem
  static const double space24 = 96; // 6rem

  // Common padding values
  static const double paddingXS = space2;  // 8
  static const double paddingSM = space3;  // 12
  static const double paddingMD = space4;  // 16
  static const double paddingLG = space6;  // 24
  static const double paddingXL = space8;  // 32

  // Common gap values
  static const double gapXS = space1;  // 4
  static const double gapSM = space2;  // 8
  static const double gapMD = space3;  // 12
  static const double gapLG = space4;  // 16
  static const double gapXL = space6;  // 24

  // Screen padding
  static const double screenPaddingHorizontal = space4; // 16
  static const double screenPaddingVertical = space6;   // 24
}
